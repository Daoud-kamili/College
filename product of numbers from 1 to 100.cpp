#include <stdio.h>

int main() {
    int pr;
    int n;
    
    printf("enter the no. whose factorila you want to find\n");
    scanf("n");

    
    for (int i = 1; i <= i<=n; i++) {
        pr *= i;
    }

    
    printf("The factorial of numbers from 1 to 100 is: %d\n", &pr);

    return 0;
}
